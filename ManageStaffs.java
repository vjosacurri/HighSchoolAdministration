    private int getID() {
        int id = 0;
        Statement st;
        try {
            st = con.createStatement();
            ResultSet rs = st.executeQuery("select max(id) from staff");
            while (rs.next()) {
                id = rs.getInt(1);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ManageStaffs.class.getName()).log(Level.SEVERE, null, ex);
        }
        return id + 1;
    }

